function y = CalculateFx(a, b, x)
% function y = CalculateFx(a, b, x)
% Calculate fx given equation y = ax + b
% Input a: angular coeficient
%       b: independent factor
%       x: x position of new point
% Output y =  f(x) calculated from f(x) = ax + b
% Aeronautics Institute of Technology
% Author: Nicolas Pereira Borges - nicolas@ita.br
% Date: 25/01/201

y = a*x + b;

end